<script>
    document.addEventListener('click', function (event) {
        // Find the button even if you click the icon inside it
        const searchBtn = event.target.closest('.searcher-toggler-box');
        const closeBtn = event.target.closest('.close-search');

        if (searchBtn) {
            event.preventDefault();
            document.body.classList.add('search-active');
        }

        if (closeBtn) {
            event.preventDefault();
            document.body.classList.remove('search-active');
        }
    });
</script>
